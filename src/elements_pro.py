"""Procedural element library — 200+ generator functions.
Each function takes (rng, **params) and returns a list of parts.
Parts are dicts: {type, x, y, w, h, r, fill, stroke, stroke_width, points, ...}
The SketchGenerator.draw_composite() renders any part list."""

import math, random

# ── Helpers ──

def _c(rng, base, vary=20):
    return [max(0, min(255, c + rng.randint(-vary, vary))) for c in base]

def _between(rng, a, b):
    return rng.uniform(a, b)

def _choice(rng, items):
    return rng.choice(items)

